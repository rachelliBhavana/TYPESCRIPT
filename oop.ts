//class declaration
class arithematic
{
    //characteristics
    no1:number;
    no2:number;
    ans:number;
    //Behaviour
    addition():number
    {
        this.ans = this.no1 + this.no2;
        return this.ans;
    }
    substraction():number
    {
        this.ans = this.no1 - this.no2;
        return this.ans;
    }
}
var Div:string="B";
console.log("Marvellous : Switch case");

switch(Div)
{
    case "A":
        {
            console.log("Time: 7AM to 11 AM");
            break;
        }
    case "B":
        {
                console.log("9 AM TO 12 PM");
                break;
        }
    case "C":
        {
                console.log("Time : 1 PM TO 6 PM ");
                break;
        }
    default:
       {
                console.log("Invalid Division");
                break;
        } 
}